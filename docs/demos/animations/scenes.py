        seg_list = VGroup()
        for i, (sname, status, scolor) in enumerate(segments):
            row = VGroup()
            name_t = Text(sname, font_size=13, color=C_WHITE)
            name_t.move_to(LEFT * 3.5 + UP * (0.8 - i * 0.45))
            status_t = Text(status, font_size=11, color=scolor)
            status_t.move_to(LEFT * 1.0 + UP * (0.8 - i * 0.45))
            dot = Dot(color=scolor, radius=0.06)
            dot.move_to(LEFT * 1.8 + UP * (0.8 - i * 0.45))
            row.add(name_t, dot, status_t)
            seg_list.add(row)

        self.timed_play(
            *[FadeIn(s, shift=RIGHT * 0.1) for s in seg_list],
            run_time=0.7,
        )
        self.wait_until(seg_start(10))

        # ── Beat 10-11: Editor panel ─────────────────────────────────
        editor_bg = RoundedRectangle(
            corner_radius=0.1, width=4.5, height=3.5,
            stroke_color=GREY_D, fill_color="#181825", fill_opacity=0.8,
        )
        editor_bg.move_to(RIGHT * 3.0 + DOWN * 0.4)

        editor_title = Text("03-wizard-gui.md", font_size=12, color=C_ACCENT)
        editor_title.move_to(editor_bg.get_top() + DOWN * 0.25)

        self.timed_play(FadeIn(editor_bg), FadeIn(editor_title), run_time=0.5)
        self.wait_until(seg_start(11))

        editor_lines = [
            "The wizard is a local web",
            "GUI that helps you draft",
            "narration scripts from your",
            "existing project docs.",
            " ",
            "Run docgen wizard and it",
            "launches a Flask server...",
        ]
        editor_text = VGroup()
        left_edge = editor_bg.get_left()[0] + 0.3
        for i, line in enumerate(editor_lines):
            lt = Text(line, font_size=10, color=C_WHITE, font="Monospace")
            lt.move_to(np.array([left_edge + lt.width / 2, 0.3 - i * 0.25, 0]))
            editor_text.add(lt)

