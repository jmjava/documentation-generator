        # ── Beat 5: Checkboxes ───────────────────────────────────────
        checks = VGroup()
        for i in range(len(files)):
            check = Text("☑", font_size=12, color=C_GREEN)
            check.move_to(LEFT * 5.0 + UP * (0.5 - i * 0.3))
            checks.add(check)

