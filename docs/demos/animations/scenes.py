# Scene 01 — Architecture overview  (~80s audio, 13 Whisper segments)
# ═══════════════════════════════════════════════════════════════════════

class DocgenOverviewScene(_TimedScene):
    """Animated architecture diagram showing the docgen pipeline flow.

    Whisper segment mapping:
      0-1  (0-12.9s)   Title card
      2    (12.9-19.7s) Title shrinks; inputs appear
      3    (19.7-25.8s) Config input shown
      4-6  (26.1-45.4s) Pipeline stages build up one by one
      7    (45.4-50.9s) Input arrow; single command mention
      8    (50.9-58.9s) Output row
      9-10 (59.8-70.7s) Concat + Pages
      11   (70.7-77.8s) Single command highlight
      12   (77.8-79.9s) Final beat + fade
    """

    def construct(self):
        self.camera.background_color = C_BG
        segs = _load_timing("01-overview")

        def seg_start(i: int) -> float:
            if i < len(segs):
                return segs[i]["start"]
            return self._clock

        def seg_end(i: int) -> float:
            if i < len(segs):
                return segs[i]["end"]
            return self._clock + 2

        # ── Beat 0-1: Title card ──────────────────────────────────────
        title = Text("docgen", font_size=52, color=C_ACCENT, weight=BOLD)
        subtitle = Text(
            "Markdown  →  polished demo videos",
            font_size=22, color=C_WHITE,
        )
        subtitle.next_to(title, DOWN, buff=0.3)
        title_group = VGroup(title, subtitle).move_to(ORIGIN)

        self.timed_play(Write(title), run_time=1.2)
        self.timed_play(FadeIn(subtitle, shift=UP * 0.2), run_time=0.8)

        tagline = Text(
            "Reproducible · version-controlled · fully automated",
            font_size=16, color=GREY_B,
        )
        tagline.next_to(subtitle, DOWN, buff=0.4)
        self.wait_until(seg_start(1))
        self.timed_play(FadeIn(tagline, shift=UP * 0.15), run_time=0.7)
        title_group.add(tagline)

        self.wait_until(seg_start(2))

        # ── Beat 2: Title shrinks; show inputs ────────────────────────
        self.timed_play(
            title_group.animate.scale(0.55).to_edge(UP, buff=0.3),
            run_time=0.8,
        )

        md_box = _box("Narration\n(.md)", C_GREEN, w=2.0, h=1.0, fs=16)
        md_box.move_to(LEFT * 5.5 + UP * 1.2)
        self.timed_play(FadeIn(md_box, shift=RIGHT * 0.3), run_time=0.6)

        # ── Beat 3: Config input ──────────────────────────────────────
        self.wait_until(seg_start(3))
        yaml_box = _box("docgen.yaml", C_ORANGE, w=2.0, h=0.7, fs=15)
        yaml_box.move_to(LEFT * 5.5 + DOWN * 0.3)
        self.timed_play(FadeIn(yaml_box, shift=RIGHT * 0.3), run_time=0.5)
        self.wait_until(seg_start(4))

        # ── Beats 4-6: Pipeline stages ────────────────────────────────
        stages = [
            ("TTS", C_PURPLE, "OpenAI\ngpt-4o-mini-tts"),
            ("Manim", C_TEAL, "Animations"),
            ("VHS", C_GREEN, "Terminal\nrecordings"),
            ("Compose", C_BLUE, "ffmpeg"),
            ("Validate", C_RED, "Drift / lint"),
        ]

        stage_boxes = []
        x_start = -3.0
        x_gap = 2.0
        for i, (label, color, desc) in enumerate(stages):
            box = _box(label, color, w=1.7, h=0.7, fs=15)
            box.move_to(RIGHT * (x_start + i * x_gap) + UP * 1.2)
            desc_text = Text(desc, font_size=11, color=color).next_to(box, DOWN, buff=0.15)
            stage_boxes.append(VGroup(box, desc_text))

        stage_span = seg_end(6) - seg_start(4)
        per_stage = max(0.4, stage_span / (len(stages) * 2))

        for i, sb in enumerate(stage_boxes):
            self.timed_play(FadeIn(sb, shift=DOWN * 0.2), run_time=per_stage)
            if i < len(stage_boxes) - 1:
                arr = _arrow(
                    sb[0].get_right(),
                    stage_boxes[i + 1][0].get_left(),
                    color=C_WHITE,
                )
                self.timed_play(Create(arr), run_time=per_stage * 0.6)

        # ── Beat 7: Input arrow + single command ──────────────────────
        self.wait_until(seg_start(7))
        input_arr = _arrow(md_box.get_right(), stage_boxes[0][0].get_left(), color=C_GREEN)
        self.timed_play(Create(input_arr), run_time=0.5)
        self.wait_until(seg_start(8))

        # ── Beat 8: Output row ────────────────────────────────────────
        outputs_label = Text("Outputs", font_size=18, color=C_ACCENT)
        outputs_label.move_to(DOWN * 1.5 + LEFT * 5)

        out_items = [
            ("audio/*.mp3", C_PURPLE),
            ("recordings/*.mp4", C_BLUE),
            ("full-demo.mp4", C_ACCENT),
            ("index.html", C_TEAL),
        ]
        out_group = VGroup()
        for j, (olabel, ocolor) in enumerate(out_items):
            ot = Text(olabel, font_size=14, color=ocolor)
            ot.move_to(RIGHT * (-3.0 + j * 2.8) + DOWN * 1.5)
            out_group.add(ot)

        self.timed_play(FadeIn(outputs_label), run_time=0.4)

        out_per = max(0.3, (seg_start(9) - self._clock - 1.0) / len(out_group))
        for o in out_group:
            self.timed_play(FadeIn(o, shift=UP * 0.15), run_time=out_per)

        output_arrows = VGroup()
        for sb in [stage_boxes[-1]]:
            arr = _arrow(sb[0].get_bottom(), outputs_label.get_top(), color=C_ACCENT)
            output_arrows.add(arr)
        self.timed_play(Create(output_arrows[0]), run_time=0.5)

        self.wait_until(seg_start(9))

        # ── Beats 9-10: Concat + Pages ────────────────────────────────
        concat_box = _box("Concat", C_ACCENT, w=1.6, h=0.6, fs=15)
        concat_box.move_to(RIGHT * 4.5 + DOWN * 0.5)
        pages_box = _box("Pages", C_TEAL, w=1.6, h=0.6, fs=15)
        pages_box.move_to(RIGHT * 4.5 + DOWN * 1.5)

        val_to_concat = _arrow(
            stage_boxes[-1][0].get_right() + DOWN * 0.1,
            concat_box.get_left(),
            color=C_ACCENT,
        )
        concat_to_pages = _arrow(
            concat_box.get_bottom(), pages_box.get_top(), color=C_TEAL,
        )

        self.timed_play(Create(val_to_concat), FadeIn(concat_box), run_time=0.5)
        self.timed_play(Create(concat_to_pages), FadeIn(pages_box), run_time=0.5)

        checks_group = VGroup()
        check_items = [
            ("✓ stream presence", C_GREEN),
            ("✓ A/V drift < 2.75s", C_GREEN),
            ("✓ no blank frames", C_GREEN),
            ("✓ narration lint clean", C_GREEN),
        ]
        for ci, (cl, cc) in enumerate(check_items):
            ct = Text(cl, font_size=12, color=cc)
            ct.move_to(LEFT * 5.0 + DOWN * (1.8 + ci * 0.35))
            checks_group.add(ct)

        check_per = max(0.3, (seg_start(11) - self._clock - 0.5) / len(check_items))
        for ct in checks_group:
            self.timed_play(FadeIn(ct, shift=RIGHT * 0.2), run_time=check_per)

        self.wait_until(seg_start(11))

        # ── Beat 11: Single command highlight ─────────────────────────
        cmd = Text(
            "docgen generate-all",
            font_size=24, color=C_ACCENT, weight=BOLD,
        )
        cmd_bg = RoundedRectangle(
            corner_radius=0.12, width=cmd.width + 0.6, height=cmd.height + 0.4,
            stroke_color=C_ACCENT, fill_color=C_BG, fill_opacity=0.9,
        )
        cmd_group = VGroup(cmd_bg, cmd).move_to(DOWN * 3.0)

        self.timed_play(FadeIn(cmd_group, shift=UP * 0.3), run_time=0.7)
        self.timed_play(Indicate(cmd_group, color=C_ACCENT, scale_factor=1.05), run_time=0.8)

        config_items = VGroup()
        cfg_entries = [
            ("segments:", C_ACCENT),
            ("  visual_map:", C_TEAL),
            ("  tts:", C_PURPLE),
            ("  validation:", C_RED),
            ("  wizard:", C_ORANGE),
        ]
        for ci, (cl, cc) in enumerate(cfg_entries):
            ct = Text(cl, font_size=12, color=cc, font="Monospace")
            ct.move_to(RIGHT * 4.0 + DOWN * (1.8 + ci * 0.3))
            config_items.add(ct)

        cfg_label = Text("docgen.yaml", font_size=14, color=C_ORANGE, weight=BOLD)
        cfg_label.move_to(RIGHT * 4.0 + DOWN * 1.4)
        self.timed_play(FadeIn(cfg_label), run_time=0.3)

        cfg_per = max(0.2, (seg_start(12) - self._clock - 0.3) / len(config_items))
        for ct in config_items:
            self.timed_play(FadeIn(ct, shift=RIGHT * 0.15), run_time=cfg_per)

        self.wait_until(seg_start(12))

        # ── Beat 12: Final + fade out ─────────────────────────────────
        self.wait_until(seg_end(12) - 1.5)
        self.timed_play(*[FadeOut(m) for m in self.mobjects], run_time=1.0)
        self.timed_wait(0.5)


