def _load_timing(segment_key: str) -> list[dict]:
    """Return Whisper segments from timing.json for a given segment key."""
    timing_path = Path(__file__).parent / "timing.json"
    if not timing_path.exists():
        return []
    data = json.loads(timing_path.read_text())
    return data.get(segment_key, {}).get("segments", [])


def _load_timing_words(segment_key: str) -> list[dict]:
    """Return Whisper word-level entries from timing.json (``words`` list)."""
    timing_path = Path(__file__).parent / "timing.json"
    if not timing_path.exists():
        return []
    data = json.loads(timing_path.read_text())
    block = data.get(segment_key) or {}
    words = block.get("words")
    return list(words) if isinstance(words, list) else []


def _box(label, color, w=2.2, h=0.75, fs=18):
    r = RoundedRectangle(
        corner_radius=0.15, width=w, height=h,
        stroke_color=color, fill_color=color, fill_opacity=0.2,
    )
    t = Text(label, font_size=fs, color=color)
    t.move_to(r.get_center())
    return VGroup(r, t)


def _arrow(start, end, color="#cdd6f4"):
    return Arrow(start, end, color=color, stroke_width=2, buff=0.15, max_tip_length_to_length_ratio=0.12)


class _TimedScene(Scene):
    """Base with a clock that tracks elapsed scene time."""

    def setup(self):
        self._clock = 0.0

    def timed_play(self, *animations, run_time=1.0, **kwargs):
        self.play(*animations, run_time=run_time, **kwargs)
        self._clock += run_time

    def wait_until(self, target: float):
        gap = target - self._clock
        if gap > 0.05:
            self.wait(gap)
            self._clock += gap

    def timed_wait(self, duration: float):
        if duration > 0.05:
            self.wait(duration)
            self._clock += duration

    def wait_until_word(self, words, index: int):
        """Wait until Whisper ``words[index]['start']`` (no-op if index invalid)."""
        if not words or index < 0 or index >= len(words):
            return
        try:
            t = float(words[index].get("start", 0.0))
        except (TypeError, ValueError):
            return
        self.wait_until(t)

    def pace_to_beat(self, segments, beat_index, num_beats, segment_indices=None):
        if not segments or num_beats <= 0:
            return
        n = len(segments)
        if segment_indices is not None:
            if beat_index < 0 or beat_index >= num_beats:
                return
            if beat_index >= len(segment_indices):
                return
            j = int(segment_indices[beat_index])
            j = max(0, min(n - 1, j))
            self.wait_until(float(segments[j].get("start", 0.0)))
            return
        if n == 1:
            self.wait_until(float(segments[0].get("start", 0.0)))
            return
        si = min(int(round(beat_index * (n - 1) / max(num_beats - 1, 1))), n - 1)
        self.wait_until(float(segments[si].get("start", 0.0)))

    def paced_reveal(self, segments, mobjects, segment_indices, run_time=0.55):
        if not mobjects:
            return
        n_m = len(mobjects)
        indices = list(segment_indices) if segment_indices is not None else []
        if not segments:
            for m in mobjects:
                self.timed_play(FadeIn(m), run_time=run_time)
            return
        for k, m in enumerate(mobjects):
            if k < len(indices):
                self.pace_to_beat(segments, k, len(indices), indices)
            else:
                self.pace_to_beat(segments, k, n_m)
            self.timed_play(FadeIn(m), run_time=run_time)

