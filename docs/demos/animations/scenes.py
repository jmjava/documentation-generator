            ("02-init-scaffold", "approved", C_GREEN),
            ("03-wizard-gui", "editing", C_ORANGE),
            ("04-tts-pipeline", "draft", C_PURPLE),
            ("05-compose-validate", "pending", GREY_B),
            ("06-ci-integration", "pending", GREY_B),
